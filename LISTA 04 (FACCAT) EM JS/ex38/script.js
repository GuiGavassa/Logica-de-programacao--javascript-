let codigo = parseInt(prompt("Digite seu código: "));

let codigoC = 1234;
let senhaC = 9999;

if (codigo != codigoC) {
    document.write("Usuário inválido! ");
}
else{
    var senha = parseInt(prompt("Digite sua senha: "));
}
if (senha != senhaC) {
    document.write("Senha incorreta!")
} 
else {
    document.write("Acesso permitido!")
}
    

